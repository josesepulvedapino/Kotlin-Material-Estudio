package com.example.kotlinesenciallinkedin

fun main() {
    //Tipos de datos basicos : Numeros en coma flotante//
    //Numeros muy grandes o numeros muy chicos//

    //Tipo Float
    val miFloat = 2.6f //Para indicar que es de tipo float, se agrega una f al final del numero (32/bits)
    //Precision de 6 a 7 cifras deficmales
    val otroFloat = 2.123456789f
    println("Solo llega hasta el 7 la variable $otroFloat")

    //Tipo Double
    val miDouble = 2.6 //Para indicar que es de tipo double solamente se escribe el decimal (64/bits)
    //Precision de 15 a 16 cifras decimales
    val otroDouble = 2.123456789
    println("Solo  hasta el 9 la variable $otroDouble")


}